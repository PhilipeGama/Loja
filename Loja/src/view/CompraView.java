    private void jtb_ProdutoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtb_ProdutoMouseClicked
        // TODO add your handling code here:
        indexProduto = jtb_Produto.getSelectedRow();
        if(indexProduto>=0 && indexProduto<modelo.getColumnCount()){
            String temp[] = modelo.getAluno(indexProduto);
            jtf_Id.setText(temp[0]);
            jtf_Produto.setText(temp[1]);  
        }
    }//GEN-LAST:event_jtb_ProdutoMouseClicked

    private void jbt_ConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbt_ConfirmarActionPerformed
        // TODO add your handling code here:
        if(jtf_Qtd.getText().equals("") || jtf_Produto.getText().equals("") || jtf_Id.getText().equals(""))
        {
            JOptionPane.showMessageDialog(null, "Insira todos os dados!");
            return;
        }
        //if(cont == 0){
           
        //}
        try {
            int quant = Integer.parseInt(jtf_Qtd.getText());
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Insira somente nÃºmeros!\n\n " + e.toString());
            return;
        }
        idC = jtf_Id.getText();
        qtd.add(jtf_Qtd.getText());
            idProduto.add(jtb_Produto.getSelectedRow());
        
            jcb_Carrinho.setSelectedItem(jtf_Produto.getText());


            jcb_Carrinho.addItem(jtf_Produto.getText());
        
        
        
    }//GEN-LAST:event_jbt_ConfirmarActionPerformed

