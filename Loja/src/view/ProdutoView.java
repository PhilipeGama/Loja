    private void jtb_ProdMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtb_ProdMouseClicked
        //TODO add your handling code here:
        int index = jtb_Prod.getSelectedRow();
        if(index >= 0 && index<modelo.getRowCount()){
            String temp[] = modelo.getAluno(index);
            jtf_Id.setText(temp[0]);
            jtf_Nome1.setText(temp[1]);
            jtf_Quant.setText(temp[4]);
            jtf_Preco.setText(temp[2]);
        }
        //Botoes(true, true, true, false, false);
    }//GEN-LAST:event_jtb_ProdMouseClicked

