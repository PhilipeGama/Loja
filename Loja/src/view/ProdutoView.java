    private void jbt_Salvar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbt_Salvar1ActionPerformed
        // TODO add your handling code here:
        int i = jtb_Prod.getSelectedRow();
        int ver = 0;
        if(jtb_Prod.isRowSelected(i))
        {
           ProdutoControl.salvarProduto(jtb_Prod.getSelectedRow(),jtf_Id.getText(),jtf_Nome1.getText(),jtf_Preco.getText(), String.valueOf(jcb_tipos.getSelectedItem()),jtf_Quant.getText());
        }
        else
        {
            ver = ProdutoControl.salvarProduto(jtb_Prod.getSelectedRow(),String.valueOf(ProdutoControl.novoId()+1),jtf_Nome1.getText(),jtf_Preco.getText(),String.valueOf(jcb_tipos.getSelectedItem()),jtf_Quant.getText());
        }
        
        
        
        switch (ver) {
            case 1:
                JOptionPane.showMessageDialog(null, "Preencha todos os campos!");
                break;
            case 0:
                JOptionPane.showMessageDialog(null, "Cadastro realizado com sucesso!");
                break;
            case 2:
                JOptionPane.showMessageDialog(null, "Dados inseridos inv√°lidos!");
                break;
            default:
                break;
        }
        jtf_Nome1.setText("");
        jtf_Preco.setText("");
        jtf_Quant.setText("");

        //Botoes(true, false, false, true, true);
        this.loadTable();
    }//GEN-LAST:event_jbt_Salvar1ActionPerformed

